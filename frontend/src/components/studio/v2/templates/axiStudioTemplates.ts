export function templateToStudioSnapshot(template: AxiTemplateDefinition) {
  return {
    mode: template.templateJson.mode,
    prompt: template.templateJson.prompt,
    duration: template.templateJson.duration,
    layers: template.templateJson.layers,
    clips: template.templateJson.clips,
    selectedLayerId: template.templateJson.editable_regions[0]?.layer_id ?? template.templateJson.layers[0]?.id ?? null,
  };
}

export function catalogItemToTemplate(item: StudioTemplateCatalogItem): AxiTemplateDefinition {
  const gradients: Record<string, string> = {
    Hotelaria: 'linear-gradient(135deg,#0f172a,#164e63 52%,#ff6a00)',
    Marketing: 'linear-gradient(135deg,#111827,#A020F0 52%,#00F0FF)',
    Restaurante: 'linear-gradient(135deg,#1c1917,#7c2d12 52%,#FF6A00)',
  };
  const templateJson = item.template_json as AxiTemplateDefinition['templateJson'];
  return {
    id: item.id,
    name: item.name,
    category: item.category,
    type: item.type,
    premium: item.premium,
    thumbnailGradient: gradients[item.category] ?? 'linear-gradient(135deg,#07111f,#12313d 52%,#00F0FF)',
    previewLabel: `${templateJson.canvas?.width ?? 1080}x${templateJson.canvas?.height ?? 1920}`,
    templateJson,
  };
}
