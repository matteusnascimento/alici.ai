export type AxiTemplateType = 'photo' | 'video' | 'social' | 'ad';

export interface AxiTemplateDefinition {
  id: string;
  name: string;
  category: string;
  type: AxiTemplateType;
  premium: boolean;
  thumbnailGradient: string;
  previewLabel: string;
  templateJson: {
    canvas: { width: number; height: number; background: string };
    prompt: string;
    duration: number;
    mode: AxiStudioMode;
    layers: AxiStudioLayer[];
    clips: AxiStudioClip[];
    assets: Array<{ id: string; asset_type: string; asset_url: string; role: string }>;
    animations: Array<{
      layer_id: string;
      property: 'x' | 'y' | 'opacity' | 'scale' | 'rotation';
      keyframes: Array<{ time: number; value: number }>;
    }>;
    fonts: Array<{ family: string; weight: number }>;
    audio: Array<{ id: string; label: string; start: number; duration: number; role: string }>;
    editable_regions: Array<{ layer_id: string; label: string; kind: 'text' | 'image' | 'color' | 'cta' }>;
  };
}

